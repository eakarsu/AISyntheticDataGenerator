// 4. NON-VIZ: Generation rules CRUD (privacy/utility tradeoffs)
router.get('/rules', (req, res) => {
  res.json({ ok: true, rules: _rules, count: _rules.length });
});

router.post('/rules', (req, res) => {
  const b = req.body || {};
  const name = (b.name || '').toString().slice(0, 120);
  if (!name) return res.status(400).json({ ok: false, error: 'name required' });
  const rule = {
    id: `rule-${_idCounter++}`,
    name,
    privacy: Math.max(0, Math.min(1, Number(b.privacy) || 0.7)),
    utility: Math.max(0, Math.min(1, Number(b.utility) || 0.7)),
    epsilon: Number(b.epsilon) || 1.0,
    notes: (b.notes || '').toString().slice(0, 500),
  };
  _rules.push(rule);
  res.status(200).json({ ok: true, rule, count: _rules.length });
});

router.put('/rules/:id', (req, res) => {
  const idx = _rules.findIndex(r => r.id === req.params.id);
  if (idx === -1) return res.status(404).json({ ok: false, error: 'not found' });
  const b = req.body || {};
  _rules[idx] = {
    ..._rules[idx],
    ...(b.name !== undefined ? { name: String(b.name).slice(0, 120) } : {}),
    ...(b.privacy !== undefined ? { privacy: Math.max(0, Math.min(1, Number(b.privacy))) } : {}),
    ...(b.utility !== undefined ? { utility: Math.max(0, Math.min(1, Number(b.utility))) } : {}),
    ...(b.epsilon !== undefined ? { epsilon: Number(b.epsilon) } : {}),
    ...(b.notes !== undefined ? { notes: String(b.notes).slice(0, 500) } : {}),
  };
  res.json({ ok: true, rule: _rules[idx] });
});

router.delete('/rules/:id', (req, res) => {
  const before = _rules.length;
  _rules = _rules.filter(r => r.id !== req.params.id);
  res.json({ ok: true, deleted: before - _rules.length, count: _rules.length });
});

module.exports = router;
